#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	*ptr;

	i = 0;
	while(s[i] != '\0')
	{
		if (s[i] == (char)c)
		{
			ptr = (char*)s + i;
			return (ptr);
		}
		i++;
	}
	if (s[i] == (char)c)
	{
		ptr = (char*)s + i;
		return (ptr);
	}
	return (NULL);
}
