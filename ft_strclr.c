#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;
	size_t	max;
	
	if (s != NULL)
	{
		max = sizeof(s) / sizeof(*s);
		i = 0;
		while(i < max)
			s[i++] = '\0';
	}
}
