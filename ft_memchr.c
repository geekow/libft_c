#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*w;
	size_t		i;

	w = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (w[i] == (unsigned char)c)
			return (&w[i]);
		i++;
	}
	return (NULL);
}
